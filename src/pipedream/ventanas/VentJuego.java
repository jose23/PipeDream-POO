    @Override
    public void notifyOfThreadComplete() {
        if(juego.getEstado() != EstadoDeJuego.SALIO){
            JOptionPane.showMessageDialog(this, (juego.getEstado() == EstadoDeJuego.GANO) ? "Fant√°stico!!\nHas logrado " + jugador.getPuntaje() + " puntos": "Has sido derrotado...", "Termino el juego", JOptionPane.INFORMATION_MESSAGE,
                    (juego.getEstado() == EstadoDeJuego.GANO) ? new javax.swing.ImageIcon(getClass().getResource("/imagenes/White_Luciendose.gif")) 
                            : new javax.swing.ImageIcon(getClass().getResource("/imagenes/White_Perdio.gif")));
        }
        VentPrincipal ventanaPrincipal = new VentPrincipal();
        ventanaPrincipal.setVisible(true);
        this.dispose();
    }

    @Override
    public void changeImage(Jugada jugada) {
       setImage(jugada.getX(), jugada.getY());
    }
    
    public void setImage(int x, int y){
        JLabel jLabel = matriz.getLabel(x, y);
        Canio canio = juego.getTablero().getTablero()[x][y];
        
        String dir;
        if(canio == null)
            dir = "/imagenes/" + "Nada" + ".png";
        else
            dir = "/imagenes/" + "Canio" + canio + ".png";
            
        jLabel.setIcon(imagenes.getImagen(dir));
        
        
    }
    
    private void llenarColaCanios(){
        canios = (juego.getCola().toArray(canios));
        for (int i = 0; i < 5; i++) {
            String dir = "/imagenes/Canio" + canios[i] + ".png";
            
            colaCanios[i].setIcon(imagenes.getImagen(dir));
        }
    }
